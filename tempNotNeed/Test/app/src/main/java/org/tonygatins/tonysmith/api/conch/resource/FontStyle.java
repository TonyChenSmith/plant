package org.tonygatins.tonysmith.api.conch.resource;

/**
 * 字体类型枚举。（以后再更新）
 * @author Tony Chen Smith
 */
public final enum FontStyle
{
	/**
	 * 黑体。
	 */
	BLACK,
	/**
	 * 黑体加斜体。
	 */
	BLACK_ITALIC,
	/**
	 * 中等体。
	 */
	MEDIUM,
	/**
	 * 中等体加斜体。
	 */
	MEDIUM_ITALIC,
	/**
	 * 轻体。
	 */
	LIGHT,
	/**
	 * 轻体加斜体。
	 */
	LIGHT_ITALIC,
	/**
	 * 瘦体。
	 */
	THIN,
	/**
	 * 瘦体加斜体。
	 */
	THIN_ITALIC,
	/**
	 * 粗体。
	 */
	BOLD,
	/**
	 * 斜体。
	 */
	ITALIC,
	/**
	 * 粗体加斜体。
	 */
	BOLD_ITALIC,
	/**
	 * 正常。
	 */
	NORMAL,
	
}
